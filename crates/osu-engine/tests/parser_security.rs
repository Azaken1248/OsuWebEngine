/// A replay for a ruleset we do not implement must be rejected, not
/// misinterpreted as osu! Standard.
#[test]
fn unknown_game_mode_is_rejected() {
    match parse_osr(&read("invalid_magic.osr"), Some(14)) {
        Err(EngineError::InvalidGameMode { mode: 99, .. }) => {}
        other => panic!("expected InvalidGameMode, got {other:?}"),
    }
}

/// Coordinates far outside the playfield must not panic the `.osu` parser.
///
/// lazer bounds coordinates at MAX_COORDINATE_VALUE and throws past it; either
/// rejecting or parsing to a finite value is acceptable, panicking is not.
#[test]
fn extreme_coordinates_do_not_panic() {
    let text = String::from_utf8(read("negative_coords.osu")).expect("fixture is UTF-8");

    if let Ok(b) = parse_osu(&text) {
        for o in &b.hit_objects {
            assert!(o.x.is_finite() && o.y.is_finite());
        }
    }
}

/// A beatmap missing every section the engine needs must be a typed error.
#[test]
fn missing_sections_is_rejected() {
    let text = String::from_utf8(read("missing_sections.osu")).expect("fixture is UTF-8");
    assert!(
        parse_osu(&text).is_err(),
        "a beatmap with no [Difficulty]/[HitObjects] should not parse"
    );
}
